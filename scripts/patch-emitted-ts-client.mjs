// Post-emit patches for @typespec/http-client-js (currently at 0.14.x alpha).
// Each patch targets a confirmed alpha-emitter bug with a citation.
// Delete patches when upstream ships the fix — don't let drift compound.

import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "../../..");
const API_FILE = resolve(ROOT, "packages/qyl-client/src/generated/api.ts");

if (!existsSync(API_FILE)) {
  console.log("patch-emitted-ts-client: generated api.ts not found, skipping");
  process.exit(0);
}

let content = readFileSync(API_FILE, "utf8");
const original = content;

// ---------------------------------------------------------------------------
// Patch 1 — Deduplicate enum value-object + type-alias pairs.
//
// Bug: @typespec/http-client-js emits the same TypeSpec enum multiple times
// when referenced from multiple operation groups. Causes TS2451/TS2300.
// Fix: scan lines, track seen "export const XValues = {" headers, skip
// the entire block (until closing "};") on subsequent occurrences, then
// also skip the following "export type X = ..." alias line.
// ---------------------------------------------------------------------------
{
  const lines = content.split("\n");
  const seenEnumNames = new Set();
  const out = [];
  let skipUntilBrace = false; // skipping inside a duplicate const block
  let skipNextTypeAlias = false; // skip the type alias that follows the const

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];

    if (skipUntilBrace) {
      if (line === "};") {
        skipUntilBrace = false;
        skipNextTypeAlias = true; // also drop the blank line + type alias after
      }
      continue; // drop this line
    }

    if (skipNextTypeAlias) {
      if (line === "" || line.startsWith("export type ")) {
        // Drop the blank line separating const from type, and the type alias
        continue;
      }
      skipNextTypeAlias = false;
    }

    const m = line.match(/^export const (\w+Values) = \{$/);
    if (m) {
      const name = m[1];
      if (seenEnumNames.has(name)) {
        skipUntilBrace = true;
        continue; // drop the opening line too
      }
      seenEnumNames.add(name);
    }

    out.push(line);
  }

  content = out.join("\n");
}

// ---------------------------------------------------------------------------
// Patch 2 — Replace concrete CursorPage interfaces with a proper generic.
//
// Bug: @typespec/http-client-js emits CursorPage<T> as multiple concrete
// interfaces with the same name. TypeScript merges them but conflicting
// `items` types cause TS2717. CursorPage is declared but never referenced
// inside the generated file, so it's safe to collapse.
// ---------------------------------------------------------------------------
{
  // Extract the structure from the first CursorPage block (items aside).
  const firstMatch = content.match(/^export interface CursorPage \{([\s\S]*?)\}\n/m);
  if (firstMatch) {
    const firstBody = firstMatch[1].replace(/^(\s+items: )[^\n]+;/m, "$1T[];");
    const generic = `export interface CursorPage<T = unknown> {${firstBody}}\n`;
    // Remove all concrete CursorPage declarations.
    content = content.replace(/^export interface CursorPage \{[\s\S]*?\}\n/gm, "");
    // Insert the generic once near the top, after the first auto-generated comment.
    const insertAfter = /^\/\/ <auto-generated\/>[^\n]*\n/m;
    content = content.replace(insertAfter, (m) => `${m}\n${generic}`);
  }
}

// ---------------------------------------------------------------------------
// Patch 3 — Deduplicate LogStats naming collision.
//
// Bug: two TypeSpec models in different namespaces are both named LogStats.
// The emitter flattens them into one file. Rename the second to LogCountStats.
// ---------------------------------------------------------------------------
{
  let count = 0;
  content = content.replace(/^export interface LogStats \{/gm, (m) => {
    count++;
    return count > 1 ? "export interface LogCountStats {" : m;
  });
}

// ---------------------------------------------------------------------------
// Patch 4 — Inject missing union type aliases.
//
// Bug: @typespec/http-client-js emits the variant interfaces (LogBodyString
// etc.) but not the top-level union type (LogBody). Same for AttributeValue.
// These are used as property types in other emitted interfaces.
// ---------------------------------------------------------------------------
{
  const injections = [];

  if (!content.includes("export type AttributeValue ")) {
    injections.push(
      "export type AttributeValue = string | boolean | number | null | AttributeValue[] | Record<string, unknown>;"
    );
  }

  if (!content.includes("export type LogBody ")) {
    injections.push(
      "export type LogBody = LogBodyString | LogBodyKvList | LogBodyArray | LogBodyBytes;"
    );
  }

  if (injections.length > 0) {
    content +=
      "\n// Injected by patch-emitted-ts-client.mjs — alpha emitter omits union type aliases.\n" +
      injections.join("\n") +
      "\n";
  }
}

// ---------------------------------------------------------------------------

if (content !== original) {
  writeFileSync(API_FILE, content, "utf8");
  console.log(`patch-emitted-ts-client: patched ${API_FILE}`);
} else {
  console.log("patch-emitted-ts-client: no changes needed");
}
